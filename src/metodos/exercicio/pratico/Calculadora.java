package metodos.exercicio.pratico;

public class Calculadora {
    public static void soma (double numero1,double numero2){
        double resultado = numero1+numero2;
        System.out.println("A soma dos dois numero e: "+resultado);

    }
    public static void subtracao (double numero1,double numero2){
        double resultado = numero1-numero2;
        System.out.println("A subtracao dos dois numero e: "+resultado);
    }
    public static void multiplicacao (double numero1,double numero2){
        double resultado = numero1*numero2;
        System.out.println("A multiplicacao dos dois numero e: "+resultado);
    }
    public static void divisao (double numero1,double numero2){
        double resultado = numero1/numero2;
        System.out.println("A divisao dos dois numero e: "+resultado);
    }
}
